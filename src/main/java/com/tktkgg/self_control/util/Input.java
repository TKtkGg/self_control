package com.tktkgg.self_control.util;

import java.util.Scanner;

public class Input {
	private static final Scanner sc = new Scanner(System.in);
	
	public static String next() {
		return sc.next();
	}
	
	public static int nextInt() {
		return sc.nextInt();
	}
	
	public static String nextLine() {
		return sc.nextLine();
	}
}
